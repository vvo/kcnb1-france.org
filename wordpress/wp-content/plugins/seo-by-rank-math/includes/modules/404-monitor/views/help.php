<h3>404 Monitor</h3>
<p>404 errors happen when someone requests a page or file that doesn't exist - actually, it's more accurate to say the web server can't find the file.</p>

<h4>404s happen when:</h4>

<ol>
	<li>Someone types the wrong address</li>
	<li>Someone links to the wrong address</li>
	<li>The file or page moves to a different address</li>
	<li>The page or file is deleted</li>
	<li>The page or file never existed</li>
</ol>

<p>They're actually very common on the web. And they're never a good thing.</p>

<p>If you visit a site, you probably want to read or view content on that site. You won't stay on the site if the content is missing. Serving 404 errors to your visitors is a good way to lose them.</p>

<p>It doesn't matter if it's your fault, their fault, or someone else's. Maybe they clicked on a broken link caused by a typing mistake on another website. Maybe they cut-and-pasted a URL wrong. Or possibly they made a spelling mistake when they typed a URL manually.</p>

<p>Maybe you changed the URL of an article to make it more appealing to the search engines.</p>

<p>Whatever the cause, the result is a disappointed reader. So fixing 404 errors is a usability issue. Patching them up will result in more useful traffic to your site, and for many sites, that means more revenue.</p>

